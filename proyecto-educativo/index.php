    <h1>Objetivos</h1>
    
    <p>Los objetivos del proyecto educativo de la escuela Agrotecnica 717 son los siguientes: </p>
    
    <ul>
      <li>Formar jóvenes profesionales en el área agropecuaria, con los conocimientos y habilidades necesarios para desempeñarse en el mercado laboral.</li>
      <li>Promover la sustentabilidad, a través de la formación en prácticas agrícolas y ganaderas respetuosas del medio ambiente.</li>
    </ul>
    
    <h1>Estructura curricular</h1>
    
    <p>El plan de estudios de la escuela Agrotecnica 717 está organizado en dos ciclos: básico, y Orientado.</p>
    
    <p>El ciclo básico (1º a 3º año) brinda una formación general en las áreas de ciencias naturales, ciencias sociales, matemática, lengua y literatura, educación física y educación tecnológica y otras de correspondientes al Area Tecnica como Apicultura, Cunicultura entre otras materias.</p>
    
    <p>El ciclo Orientado (4º a 7º año) se enfoca en la formación específica en el área agropecuaria. Los estudiantes ven las distintas asignaturas que conforman el Diseño Curricular.</p>
    
    <p>
    <strong>El título que obtienen es el de Tecnico/a en Producción Agropecuaria.</strong> <br> <br> </p>

